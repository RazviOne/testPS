package ro.tuc.ds2020.dtos;

public class TagDTO {
    private Integer id;
    private String name;

    public TagDTO() {}

    public TagDTO(Integer id, String name) {
        this.id = id;
        this.name = name;
    }

    public Integer getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }
}
